// What is a Constant?
//A constant is a value that doesn’t change once defined — like a permanent label.

package main

import "fmt"

func main() {

	const pi = 3.14
	const name string = "Saurabh"
	const age = 25

	fmt.Println("Name :- ", name)
	fmt.Println("Age :- ", age)
	fmt.Println("Value Of Pi :- ", pi)
}
